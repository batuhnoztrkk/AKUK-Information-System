    private void jButtondüzenleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtondüzenleActionPerformed
                    
                    int id = Integer.valueOf(jTextField8.getText());
                    String ad = jTextField1.getText();
                    String soyad = jTextField2.getText();
                    String kullanıcıAdı = jTextField3.getText();
                    String e_posta = jTextField4.getText();
                    String telefon = jTextField5.getText();
                    String okul_no = jTextField6.getText();
                    String şifre = jTextField7.getText();
                  
       
                             
                    if(ad.trim().equals("") || soyad.trim().equals("") || kullanıcıAdı.trim().equals("") || e_posta.trim().equals("") || telefon.trim().equals("") || okul_no.trim().equals("") || şifre.trim().equals("")) {
                          JOptionPane.showMessageDialog(null, "Bir veya Daha Fazla Alan Boş","Boş alan",2);
                         
                    }
                    else if(şifre.length()<8){
                    JOptionPane.showMessageDialog(null, "Şifre en az 8 haneli olmalıdır.Lütfen daha uzun şifre belirleyiniz.","Basit Şifre",2);
            
                    }
                    else if(okul_no.length()>12){
                    JOptionPane.showMessageDialog(null, "Okul numarası 12 haneden büyük olamaz.","Hatalı Okul Numarası",2);
            
                    }
                    else if(okul_no.length()<=11){
                    JOptionPane.showMessageDialog(null, "Okul numarası 11 haneden küçük olamaz.","Hatalı Okul Numarası",2);
            
                    }
                    else if(telefon.length()>11){
                    JOptionPane.showMessageDialog(null, "Telefon numarası 11 haneden büyük olamaz ","Hatalı Telefon Numarası",2);
            
                    }
                    else if(telefon.length()<=10){
                    JOptionPane.showMessageDialog(null, "Telefon numarası 10 haneden küçük olamaz.(Telefon numarasının başına 0 ekleyiniz.) ","Hatalı Telefon Numarası",2);
            
                    }
                    else{                                       
                    ayarlarrrrrrrrrrrrrrr ay = new ayarlarrrrrrrrrrrrrrr(id ,ad, soyad, kullanıcıAdı, e_posta, telefon, okul_no, şifre);
                    ayarquery q = new ayarquery();
                    q.updateContact(ay);
                 
                    }
        
    }//GEN-LAST:event_jButtondüzenleActionPerformed

