    public void populateJTable(){
        
        ayarquery q = new ayarquery();
        
        ArrayList <ayarlarrrrrrrrrrrrrrr> uglist = q.contactList();
        String [] colNames = {"Ad","Soyad","Kullanıcı Adı","E-posta","Telefon","Okul No","Şifre","id"};
        Object [][] rows = new Object[uglist.size()][8];
        
        for(int i=0 ; i<uglist.size() ; i++){
            
            rows[i][0] = uglist.get(i).getAd();
            rows[i][1] = uglist.get(i).getSoyad();
            rows[i][2] = uglist.get(i).getKullanıcıAdı();
            rows[i][3] = uglist.get(i).getE_posta();
            rows[i][4] = uglist.get(i).getTelefon();
            rows[i][5] = uglist.get(i).getOkul_no();
            rows[i][6] = uglist.get(i).getŞifre();
            rows[i][7] = uglist.get(i).getId();
        }
        
        MyModel mmd = new MyModel(rows , colNames);
        jTable1.setModel(mmd);
    }
    
    
    
    
    
    
    
    
    
    
